!**********************************************************************
      subroutine plcset
!**********************************************************************
      use cphcns, only : cev, cme, cmp
      use cplcom, only : c00, c10, c11, c12, c13, c14, c15, c16, c21
     >    , c23, c25, c31, c32, c34, c43, c51, c52, c53, c54, c56, c58
     >    , c73, calf, cdle, cdli, cthe, cthi
      use cunit,  only : n6
      implicit none
! 
      write(n6,'(/2x,"*** plcset ***")')
!
!-----------------------------------------------------------------------
!::numerical constant
!-----------------------------------------------------------------------
      c00=0.0d0
      c11=1.0d0
      c12=1.0d0/2.0d0
      c13=1.0d0/3.0d0
      c14=1.0d0/4.0d0
      c15=1.0d0/5.0d0
      c16=1.0d0/6.0d0
      c10=1.0d0/10.0d0
      c21=2.0d0
      c23=2.0d0/3.0d0
      c25=2.0d0/5.0d0
      c31=3.0d0/1.0d0
      c32=3.0d0/2.0d0
      c34=3.0d0/4.0d0
      c43=4.0d0/3.0d0
      c51=5.0d0
      c52=5.0d0/2.0d0
      c53=5.0d0/3.0d0
      c54=5.0d0/4.0d0
      c56=5.0d0/6.0d0
      c58=5.0d0/8.0d0
      c73=7.0d0/3.0d0
      calf=sqrt(5.0d0/3.0d0)
!
!-----------------------------------------------------------------------
!::physical constant   move to phcnst.f
!-----------------------------------------------------------------------
!x    cev = 1.60210e-19
!x    cmp = 1.67252e-27
!x    cme = 9.10908e-31
!
!-----------------------------------------------------------------------
!::thermal force
!-----------------------------------------------------------------------
      cthi = 0.0d0
      cthe = 0.0d0
!
!-----------------------------------------------------------------------
!::boundary condition
!-----------------------------------------------------------------------
      cdli = 2.5d0
      cdle = 4.0d0
!
!-----------------------------------------------------------------------
!::debug write
!-----------------------------------------------------------------------
      write(n6,'(5x,"numerical con. : c00,c12,c21 =",3f8.3)')
     >   c00,c12,c21
      write(n6,'(5x,"physical  con. : cev,cmp,cme =",1p3e12.3)')
     >   cev,cmp,cme
      write(n6,'(5x,"thermal force  : cthi,cthe   =",3f8.3)') cthi,cthe
      write(n6,'(5x,"boundary cond. : cdli,cdle,  =",2f8.3)') cdli,cdle
!
      return
      end
